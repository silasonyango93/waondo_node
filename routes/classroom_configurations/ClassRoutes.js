const express = require("express");
const router = express.Router();
const bodyParser = require("body-parser");
var urlencodedParser = bodyParser.urlencoded({ extended: false });
const ClassController = require("../../controllers/classroom_configurations/ClassController.js");

//Middle ware that is specific to this router
router.use(function timeLog(req, res, next) {
  next();
});

router.post("/add_class", urlencodedParser, function(request, response) {
  var date = new Date();
  date.setHours(date.getHours() + 0);

  var jsonObject_ = {
    LotId: request.body.LotId,
    ClassStreamId: request.body.ClassStreamId,
    RegisteredDate: date,
      IsAdminClass: 0
  };

  var myPromise = ClassController.insert(jsonObject_);

  myPromise.then(
    function(result) {
      var response_object = { results: result };
      response.send(response_object);
    },
    function(err) {
      console.log(err);
      response.send("An error occurred");
    }
  );
});

